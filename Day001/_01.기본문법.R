# 연산자(operand)
## 산술연산자: +. -, *, /(실수몫), %%(나머지), %/%(정수몫), ^ - **(거듭제곱)
(11 + 54 - 13) * 76 / 9
8 / 3 #결과값 2.666667
8 %% 3 #결과값 2
8 %/% 3 #결과값 2
4 ** 2 #결과값 16
4 ** 3 #결과값 64

